#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*k;

	i = 0;
	k = (unsigned char *) s;
	while (i < n)
	{
		*k = '\0';
		k++;
		i++;
	}
}
